    private void jbtnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnGuardarActionPerformed

            boolean Guardar = true;
            Integer IdCliente = new Integer(jTextId.getText());
            String Nombres = jTextNombres.getText();
            String Apellidos = jTextApellidos.getText();
            String Cedula = jTextCedula.getText();
            String Telefono = jTextTeléfono.getText();
            String Direccion = jTextDireccion.getText();
            String Ciudad = jTextCiudad.getText();
            
          //  if(Utilidades.EliminarEspacioBlanco(jTextId.getText()).equals("") == true)jTextId.setText("0");
            
            if(IdCliente == 0){
            
              JOptionPane.showMessageDialog(null, "Debe introducir un Id");  
              Guardar = false;
              
                              }
            if(Utilidades.EliminarEspacioBlanco(Nombres).equals("") == true){
            
              JOptionPane.showMessageDialog(null, "Debe introducir un nombre");  
              Guardar = false;
                                          
                                          }
            if(Utilidades.EliminarEspacioBlanco(Apellidos).equals("") == true){
            
              JOptionPane.showMessageDialog(null, "Debe introducir un apellido");  
              Guardar = false;
            
                                            }
            if(Utilidades.EliminarEspacioBlanco(Cedula).equals("") == true){
            
              JOptionPane.showMessageDialog(null, "Debe introducir un # de cedula");  
              Guardar = false;
            
                                         }
            if(Utilidades.EliminarEspacioBlanco(Telefono).equals("") == true){
            
              JOptionPane.showMessageDialog(null, "Debe introducir un telefono");  
              Guardar = false;
            
                                           }
            if(Utilidades.EliminarEspacioBlanco(Direccion).equals("") == true){
            
              JOptionPane.showMessageDialog(null, "Debe introducir una direccion");  
              Guardar = false;
            
                                            }
            if(Utilidades.EliminarEspacioBlanco(Ciudad).equals("") == true){
            
              JOptionPane.showMessageDialog(null, "Debe introducir una ciudad");  
              Guardar = false;
                
                                         }
            
            
            if(Guardar == true){
            
               Jbbc.Registrar_Cliente(IdCliente, Nombres, Apellidos, Cedula, Telefono, Direccion, Ciudad);
               JOptionPane.showMessageDialog(null, "Datos guardados correctamente"); 
            
                               }
        
  
        
    }//GEN-LAST:event_jbtnGuardarActionPerformed

