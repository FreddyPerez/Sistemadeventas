    private void jbtnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnModificarActionPerformed
     //   Modificar_Cliente abrir = new   Modificar_Cliente();
     //   abrir.setVisible(true);
           String IdBuscar = Utilidades.EliminarEspacioBlanco(jTextBuscar.getText());
           if(IdBuscar.equals("") == true){
           
             JOptionPane.showMessageDialog(null, "Debe digitar un id");    
           
                                          }
           else {
           
              boolean NoEsDigito = false;
              for(int a = 0;a < IdBuscar.length();a++){
              
                 
                 if(Character.isDigit(IdBuscar.charAt(a)) == false){
                 
                    NoEsDigito = true;
                    break;
                     
                                                                   }  
              
                                                      }
              if(NoEsDigito == true){
              
                 JOptionPane.showMessageDialog(null, "Debe digitar un id numerico");  
              
                                    }
              
              else {
              
           Integer IdCliente = new Integer(jTextBuscar.getText());
           Jbbc.Conectar();
           
              try{
               
                  ResultSet consulta = Jbbc.Estatuto.executeQuery("Select * from cliente where IdCliente = "+IdCliente+"");
                  
                  if(consulta.next()){
                  
                    jTextId.setText(consulta.getString("IdCliente").toString());
                    jTextNombres.setText(consulta.getString("Nombres"));
                    jTextApellidos.setText(consulta.getString("Apellidos"));
                    jTextCedula.setText(consulta.getString("Cedula"));
                    jTextTeléfono.setText(consulta.getString("Telefono"));
                    jTextDireccion.setText(consulta.getString("Direccion"));
                    jTextCiudad.setText(consulta.getString("Ciudad"));                 
                      
                                      }
                  else{
                  
                    JOptionPane.showMessageDialog(null, "El id no existe");                   
                      
                      }
                  Jbbc.Desconectar();
                  
                 }catch(SQLException ex){
              
                    System.out.println("Error al consultar");
                    Jbbc.Desconectar();
              
                                     }
                  
                   }
           
                }
           
           
        
        
    }//GEN-LAST:event_jbtnModificarActionPerformed

